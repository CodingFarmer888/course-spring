package com.course.vo;

public class CityVo {

	/** 鍵值 */
	private Long id;
	
	/** 代碼 */
	private String code;
	
	/** 名稱 */
	private String name;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
